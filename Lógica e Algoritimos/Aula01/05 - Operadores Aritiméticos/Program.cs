/*
 * Soma:             +
 * Subtrção:         -
 * Divisão:          /
 * Multiplicação:    *
 * Resto da Divisão: %
*/

Console.WriteLine(10 + 5);
Console.WriteLine(10 - 5);
Console.WriteLine(10 / 5);
Console.WriteLine(10 * 5);
Console.WriteLine(10 % 5);

// Calculo de média
